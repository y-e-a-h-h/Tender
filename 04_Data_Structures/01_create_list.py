# 定义有内容的列表
list1 = [34, 56, 78, 34, 67]
list2 = ['Beijing', 'Hello', 'Good']
list3 = [23, 'Hello', True, None]
list4 = [56, [1, 5, 9], 34.7, 'Enough', True, None]
print(list1)
print(list2)
print(list3)
print(list4)
print(type(list4))

