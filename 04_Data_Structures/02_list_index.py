# 定义一个列表
list1 = [10, 20, 30, 40, 50]
# 正索引
print(list1[0])
print(list1[1])
print(list1[2])
print(list1[3])
print(list1[4])
# 负索引
print(list1[-1])
print(list1[-2])
print(list1[-3])
print(list1[-4])
print(list1[-5])

# 定义一个嵌套列表
list2 = [10, 20, ['Yeah', 'Perfect'], 40, 50]
print(list2[0])
print(list2[1])
print(list2[2][1])
print(list2[3])
print(list2[4])
print(list2[-1])
print(list2[-2])
print(list2[-3][-2])
print(list2[-4])
print(list2[-5])
